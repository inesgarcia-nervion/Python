'''10. Diseñar una función que recibe como parámetros dos números enteros y devuelve el máximo de ambos.'''
numero1 = int(input("Introduzca un número entero: "))
numero2 = int(input("Introduzca otro número entero: "))

if numero1 > numero2:
    print(numero1, "es mayor")
else:
    print(numero2, "es mayor")